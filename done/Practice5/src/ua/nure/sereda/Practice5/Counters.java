package ua.nure.sereda.Practice5;

class Counters {
    private int firstCounter = 0;
    private int secondCounter = 0;

    void addFirstCounter() {
        firstCounter += 1;
    }

    void addSecondCounter() {
        secondCounter += 1;
    }

    synchronized void addFirstSyncCounter() {
        firstCounter += 1;
    }

    synchronized void addSecondSyncCounter() {
        secondCounter += 1;
    }

    int getFirstCounter() {
        return firstCounter;
    }

    int getSecondCounter() {
        return secondCounter;
    }
}
